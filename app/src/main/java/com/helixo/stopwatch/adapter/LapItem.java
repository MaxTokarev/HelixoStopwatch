package com.helixo.stopwatch.adapter;

public class LapItem {
    int lapNumber;
    String time;

    public void setLapNumber(int lapNumber) {
        this.lapNumber = lapNumber;
    }

    public void setTime(String time) {
        this.time = time;
    }


    public int getLapNumber() {
        return lapNumber;
    }

    public String getTime() {
        return time;
    }
}


